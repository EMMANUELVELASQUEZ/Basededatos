SELECT COUNT(P.NUMPROY) AS CANTIDAD_DE_PROYECTOS
FROM PROYECTOS P, DEPARTAMENTOS D
WHERE P.CLAVEDEPTO = D.CLAVEDEPTO
  AND D.NOMDEPTO = 'PLANIFICACION';