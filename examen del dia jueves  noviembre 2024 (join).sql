SELECT E.NOMBRE, E.NIVELEST
FROM EMPLEADOS E, DEPARTAMENTOS D
WHERE E.NUMDEPTO = D.CLAVEDEPTO
  AND D.NOMDEPTO = 'PRODUCCION DE SISTEMAS';


/*5*/  
SELECT E.APELLIDO, E.PUESTO, D.NOMDEPTO
FROM EMPLEADOS E, DEPARTAMENTOS D
WHERE E.NUMDEPTO = D.CLAVEDEPTO
  AND D.NOMDEPTO LIKE '%PLAN%';


