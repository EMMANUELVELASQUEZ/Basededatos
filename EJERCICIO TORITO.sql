/*1.*/ 
SELECT Nombre
FROM articulos
ORDER BY Nombre DESC;



/*2.*/  
SELECT FOLIO, (SUBTOTAL + IVA) AS TOTAL
FROM facturas
WHERE FECHA = CURDATE() - INTERVAL 1 WEEK;



/*3.*/
SELECT NOMCLI
FROM clientes
WHERE NOMCLI LIKE '%EZ%';


/*4.*/   
SELECT SUM(PRECIO * CANTIDAD) AS MONTO_TOTAL
FROM articulos;


SELECT SUM(PVENTA * CANTIDAD) AS MONTO_TOTAL
FROM partidas;

/*5.*/  
SELECT CVELINEA, NOMLINEA
FROM lineas
WHERE  CVELINEA > 700;


/*6.*/
SELECT NOMBRE
FROM articulos
WHERE PRECIO BETWEEN 500 AND 700;





