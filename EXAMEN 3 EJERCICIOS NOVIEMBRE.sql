/*1*/
SELECT CLAVEDEPTO, COUNT(NUMPROY) AS Total_proyectos
FROM proyectos
WHERE NUMPROY LIKE '%SOLDADURA%'
GROUP BY CLAVEDEPTO;

/*2*/
SELECT MIN(SALARIO) AS Salario_Minimo
FROM empleados
GROUP BY puesto;

/*3.*/
SELECT CLAVEDEPTO, SUM(HORASHOMBRE) AS Total_de_Horas_Hombre
FROM proyectos
GROUP BY CLAVEDEPTO
HAVING SUM(HORASHOMBRE) > 4;
